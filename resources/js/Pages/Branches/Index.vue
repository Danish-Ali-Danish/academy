  }

  // Load mobile data
  const loadMobileData = async () => {
    mobileLoading.value = true
    
    try {
      const params = {
        page: mobileCurrentPage.value,
        per_page: perPage.value,
        mobile: 1
      }
      
      if (filters.search) params.search = filters.search
      if (tableSearch.value) params.search = tableSearch.value
      if (filters.is_active !== '') params.is_active = filters.is_active
      
      const response = await axios.get(route('branches.index'), {
        params,
        headers: {
          'X-Requested-With': 'XMLHttpRequest',
          'Accept': 'application/json'
        }
      })
      
      if (response.data) {
        if (response.data.data) {
          mobileBranches.value = response.data.data
          mobileCurrentPage.value = response.data.current_page || 1
          mobileTotalPages.value = response.data.last_page || 1
          mobileTotal.value = response.data.total || 0
          mobileOffset.value = response.data.from ? response.data.from - 1 : 0
        } else if (Array.isArray(response.data)) {
          mobileBranches.value = response.data
          mobileTotalPages.value = 1
          mobileTotal.value = response.data.length
          mobileOffset.value = 0
        }
      }
    } catch (error) {
      console.error('Error loading mobile data:', error)
      mobileBranches.value = []
      mobileTotal.value = 0
    } finally {
      mobileLoading.value = false
    }
  }

  // Initialize on mount
  onMounted(() => {
    loadMobileData()

    // Initialize desktop table
    table = $('#branches-table').DataTable({
      processing: true,
      serverSide: true,
      ajax: {
        url: route('branches.index'),
        data: function(d) {
          d.search.value = filters.search || tableSearch.value
          if (filters.is_active !== '') {
            d.is_active = filters.is_active
          }
        }
      },
      columns: [
        { data: 'DT_RowIndex', name: 'DT_RowIndex', orderable: false, searchable: false },
        { data: 'branch_name', name: 'branch_name' },
        { data: 'location', name: 'location' },
        { data: 'phone', name: 'phone' },
        { data: 'classes_count', name: 'classes_count', orderable: false },
        { data: 'is_active', name: 'is_active' },
        { data: 'action', name: 'action', orderable: false, searchable: false, className: 'text-center' }
      ],
      pageLength: 10,
      lengthMenu: [[10, 25, 50, 100], [10, 25, 50, 100]],
      order: [[1, 'asc']],
      searching: true,
      info: true,
      responsive: true,
      
      dom: '<"flex items-center justify-between border-b border-gray-200"<"ml-auto"i>>rt<"flex items-center justify-between px-6 py-4 border-t border-gray-200"<"text-sm text-gray-600"i>p>',
      language: {
        emptyTable: '<div class="text-center py-12 text-gray-500"><svg class="mx-auto h-12 w-12 text-gray-400" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M19 21V5a2 2 0 00-2-2H7a2 2 0 00-2 2v16m14 0h2m-2 0h-5m-9 0H3m2 0h5M9 7h1m-1 4h1m4-4h1m-1 4h1m-5 10v-5a1 1 0 011-1h2a1 1 0 011 1v5m-4 0h4"/></svg><p class="mt-2 text-sm font-medium">No branches found</p></div>',
        info: 'Showing _START_ to _END_ of _TOTAL_ entries',
        infoEmpty: 'Showing 0 to 0 of 0 entries',
        infoFiltered: '(filtered from _MAX_ total entries)',
        processing: '<div class="flex items-center justify-center py-8"><div class="animate-spin rounded-full h-10 w-10 border-b-2 border-blue-600"></div></div>',
        paginate: {
          first: '<svg class="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M11 19l-7-7 7-7m8 14l-7-7 7-7"/></svg>',
          last: '<svg class="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M13 5l7 7-7 7M5 5l7 7-7 7"/></svg>',
          next: '<svg class="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M9 5l7 7-7 7"/></svg>',
          previous: '<svg class="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2" d="M15 19l-7-7 7-7"/></svg>'
        }
      },
      drawCallback: function () {
        const info = $('#branches-table_info')
        $('#table-info').empty().append(info)

        const paginate = $('#branches-table_paginate')
        $('#table-pagination').empty().append(paginate)
      }
    })
  })

  // View branch classes
  const viewBranchClassesMobile = async (branchId) => {
    showViewModal.value = true
    loadingViewClasses.value = true
    
    try {
      const response = await axios.get(route('branches.branch-classes', branchId))
      viewingBranch.value = response.data.branch
      viewingClasses.value = response.data
    } catch (error) {
      console.error('Error loading branch classes:', error)
    } finally {
      loadingViewClasses.value = false
    }
  }

  window.viewBranchClasses = viewBranchClassesMobile

  // Delete branch
  const confirmDelete = () => {
    deleting.value = true
    router.delete(route('branches.destroy', branchToDelete.value), {
      onSuccess: () => {
        showDeleteModal.value = false
        deleting.value = false
        loadData()
      },
      onError: () => {
        deleting.value = false
      }
    })
  }

  window.deleteBranch = (id) => {
    branchToDelete.value = id
    showDeleteModal.value = true
  }

  // Make editBranch available globally for DataTables
  window.editBranch = (branch) => {
    router.visit(route('branches.edit', branch.id))
  }

  // Mobile pagination
  const prevPage = () => {
    if (mobileCurrentPage.value > 1 && !mobileLoading.value) {
      mobileCurrentPage.value--
      loadMobileData()
      window.scrollTo({ top: 0, behavior: 'smooth' })
    }
  }

  const nextPage = () => {
    if (mobileCurrentPage.value < mobileTotalPages.value && !mobileLoading.value) {
      mobileCurrentPage.value++
      loadMobileData()
      window.scrollTo({ top: 0, behavior: 'smooth' })
    }
  }

  // Table search with debounce
  let tableSearchTimeout = null
  const tableSearchDebounced = () => {
    clearTimeout(tableSearchTimeout)
    tableSearchTimeout = setTimeout(() => {
      loadData()
    }, 500)
  }

  // Filter search with debounce
  let searchTimeout = null
  const searchDebounced = () => {
    clearTimeout(searchTimeout)
    searchTimeout = setTimeout(() => {
      loadData()
    }, 500)
  }

  // Change per page
  const changePerPage = () => {
    if (table) {
      table.page.len(perPage.value).draw()
    }
    mobileCurrentPage.value = 1
    loadMobileData()
  }

  // Reload table
  const loadData = () => {
    if (table) {
      table.ajax.reload()
    }
    mobileCurrentPage.value = 1
    loadMobileData()
  }

  // Reset filters
  const resetFilters = () => {
    filters.search = ''
    filters.is_active = ''
    tableSearch.value = ''
    loadData()
  }
  </script>

